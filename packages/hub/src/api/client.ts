export class ApiError extends Error {
  constructor(
    public status: number,
    message: string,
  ) {
    super(message);
  }
}

/** Submit a public report. Throws ApiError(429) when rate-limited. */
export async function postReport(body: SubmitReportRequest): Promise<SubmitReportResponse> {
  const res = await fetch(`${API_BASE}/reports`, {
    method: "POST",
    headers: { "content-type": "application/json", accept: "application/json" },
    body: JSON.stringify(body),
  });
  if (!res.ok) {
    const detail = (await res.json().catch(() => ({}))) as { message?: string };
    throw new ApiError(res.status, detail.message ?? `Request failed (${res.status})`);
  }
  return (await res.json()) as SubmitReportResponse;
}

// --- Community plane -------------------------------------------------------
export interface RequestFilter {
  incidentId?: string;
  segmentId?: string;
}

export function fetchRequests(filter: RequestFilter): Promise<{ requests: CommunityRequestItem[] }> {
  const params = new URLSearchParams();
  if (filter.incidentId) params.set("incidentId", filter.incidentId);
  if (filter.segmentId) params.set("segmentId", filter.segmentId);
  const qs = params.toString();
  return getJson<{ requests: CommunityRequestItem[] }>(`/requests${qs ? `?${qs}` : ""}`);
}

async function postJson<T>(path: string, body: unknown): Promise<T> {
  const res = await fetch(`${API_BASE}${path}`, {
    method: "POST",
    headers: { "content-type": "application/json", accept: "application/json" },
    body: JSON.stringify(body),
  });
  if (!res.ok) {
    const detail = (await res.json().catch(() => ({}))) as { message?: string };
    throw new ApiError(res.status, detail.message ?? `Request failed (${res.status})`);
  }
  return (await res.json()) as T;
}

export function createRequest(body: CreateRequestRequest) {
  return postJson<{ ok: boolean; request: { id: string } }>("/requests", body);
}
export function addResponse(requestId: string, body: string, deviceToken?: string) {
  return postJson<{ ok: boolean }>(`/requests/${requestId}/responses`, { body, deviceToken });
}
export async function patchRequest(requestId: string, status: "matched" | "resolved", deviceToken: string) {
  const res = await fetch(`${API_BASE}/requests/${requestId}`, {
    method: "PATCH",
    headers: { "content-type": "application/json", accept: "application/json" },
    body: JSON.stringify({ status, deviceToken }),
  });
  if (!res.ok) throw new ApiError(res.status, `Request failed (${res.status})`);
  return (await res.json()) as { ok: boolean };
}
export function flagRequest(requestId: string) {
  return postJson<{ ok: boolean }>(`/requests/${requestId}/flag`, {});
}
export function flagResponse(responseId: string) {
  return postJson<{ ok: boolean }>(`/responses/${responseId}/flag`, {});
}

