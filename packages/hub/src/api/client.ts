// --- Admin (steward bearer token) -----------------------------------------
async function adminFetch<T>(path: string, token: string, init: RequestInit = {}): Promise<T> {
  const res = await fetch(`${API_BASE}${path}`, {
    ...init,
    headers: {
      accept: "application/json",
      authorization: `Bearer ${token}`,
      ...(init.body ? { "content-type": "application/json" } : {}),
      ...(init.headers ?? {}),
    },
  });
  if (!res.ok) throw new ApiError(res.status, `Request failed (${res.status})`);
  return (await res.json()) as T;
}

export const admin = {
  reports: (token: string, state = "pending") =>
    adminFetch<{ reports: QueueReport[] }>(`/admin/reports?state=${state}`, token),
  verify: (token: string, id: string) =>
    adminFetch<{ ok: boolean }>(`/admin/reports/${id}/verify`, token, { method: "POST" }),
  dismiss: (token: string, id: string, reason?: string) =>
    adminFetch<{ ok: boolean }>(`/admin/reports/${id}/dismiss`, token, {
      method: "POST",
      body: JSON.stringify({ reason }),
    }),
  merge: (token: string, reportIds: string[], targetIncidentId: string) =>
    adminFetch<{ ok: boolean; merged: number }>(`/admin/reports/merge`, token, {
      method: "POST",
      body: JSON.stringify({ reportIds, targetIncidentId }),
    }),
  overrides: (token: string) => adminFetch<{ overrides: AdminOverride[] }>(`/admin/overrides`, token),
  createOverride: (
    token: string,
    body: { segmentId: string; status: string; reason: string; expiresAt?: string },
  ) => adminFetch<{ ok: boolean }>(`/admin/overrides`, token, { method: "POST", body: JSON.stringify(body) }),
  clearOverride: (token: string, id: string) =>
    adminFetch<{ ok: boolean }>(`/admin/overrides/${id}`, token, { method: "DELETE" }),
  audit: (token: string) => adminFetch<{ entries: AuditEntry[] }>(`/admin/audit`, token),
  removeRequest: (token: string, id: string) =>
    adminFetch<{ ok: boolean }>(`/admin/requests/${id}/remove`, token, { method: "POST" }),
  removeResponse: (token: string, id: string) =>
    adminFetch<{ ok: boolean }>(`/admin/responses/${id}/remove`, token, { method: "POST" }),
};
