/**
 * API base. Same-origin `/api` by default (the Vite dev server proxies to core,
 * and a same-host prod deploy serves both). For a static+CDN hub whose API lives
 * on another host, set `VITE_API_BASE` at build time (e.g. https://api.example).
 */
const API_BASE = (import.meta.env.VITE_API_BASE ?? "/api").replace(/\/$/, "");

/**
 * The CDN-hosted last-known-status file, regenerated by the backend on every
 * status change. It is served from the hub's own static host, so it stays
 * reachable even when the API/backend is completely down — the honest
 * stale-fallback source. Overridable with `VITE_STATIC_FALLBACK_URL`.
 */
const STATIC_FALLBACK_URL =
  import.meta.env.VITE_STATIC_FALLBACK_URL ?? `${import.meta.env.BASE_URL}status-fallback.json`;

/** Treat a slow backend as a failure so we can fall back to the cached snapshot. */
const REQUEST_TIMEOUT_MS = 6000;

async function getJson<T>(path: string, timeoutMs = REQUEST_TIMEOUT_MS): Promise<T> {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const res = await fetch(`${API_BASE}${path}`, {
      signal: controller.signal,
      headers: { accept: "application/json" },
    });
    if (!res.ok) {
      throw new Error(`Request failed: ${res.status} ${res.statusText}`);
    }
    return (await res.json()) as T;
  } finally {
    clearTimeout(timer);
  }
}

export function fetchStatus(): Promise<StatusResponse> {
  return getJson<StatusResponse>("/status");
}

export function fetchSnapshot(): Promise<SnapshotResponse> {
  return getJson<SnapshotResponse>("/status/snapshot");
}

/**
 * Fetch the CDN static fallback (last-known status) directly — bypasses API_BASE
 * so it works when the backend is down. Used only after a live fetch fails.
 */
export async function fetchStaticFallback(): Promise<SnapshotResponse> {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), 4000);
  try {
    const res = await fetch(STATIC_FALLBACK_URL, {
      signal: controller.signal,
      headers: { accept: "application/json" },
      cache: "no-store",
    });
    if (!res.ok) throw new Error(`Fallback failed: ${res.status}`);
    return (await res.json()) as SnapshotResponse;
  } finally {
    clearTimeout(timer);
  }
}

/**
 * Health report. The endpoint returns 503 (with the full report body) when the
 * system is unhealthy, so we read the JSON on both 200 and 503 and only treat a
 * genuine transport/parse failure as an error.
 */
export async function fetchHealth(): Promise<HealthReport> {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), REQUEST_TIMEOUT_MS);
  try {
    const res = await fetch(`${API_BASE}/health`, {
      signal: controller.signal,
      headers: { accept: "application/json" },
    });
    if (res.status !== 200 && res.status !== 503) {
      throw new Error(`Health request failed: ${res.status}`);
    }
    return (await res.json()) as HealthReport;
  } finally {
    clearTimeout(timer);
  }
}

